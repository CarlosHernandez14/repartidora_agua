    private void btnCreateRepaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateRepaActionPerformed
        // TODO add your handling code here:
        // Obtenemos los datos de los inputs
        String nombreCompleto = this.fieldNombreCompleto.getText();
        String telefono = this.fieldTelefono.getText();
        String email = this.fieldEmail.getText();
        String password = this.fieldPassword.getText();

        // Validamos que los campos no esten vacios
        if (nombreCompleto.isEmpty() || telefono.isEmpty() || email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son requeridos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validamos que el telefono tenga 10 digitos
        if (telefono.length() != 10) {
            JOptionPane.showMessageDialog(this, "El telefono debe tener 10 digitos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Creamos el objeto repartidor
        Repartidor repartidor = new Repartidor(nombreCompleto, telefono, nombreCompleto, email, password);

        // Guardamos el repartidor en la base de datos
        int repartidorId = WSManager.guardarRepartidor(repartidor);

        if (repartidorId != -1) {
            JOptionPane.showMessageDialog(this, "Repartidor creado con exito", "Exito", JOptionPane.INFORMATION_MESSAGE);
            this.homeOperador.loadRepartidores();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Error al crear repartidor", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnCreateRepaActionPerformed

